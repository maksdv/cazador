 	<h2>Estimado Amigo:</h2><br>

<p>Te doy la bienvenida a la web oficial de la Asociación Española del Deutscher Jagdterrier.
	<br><br>
Entiendo que has llegado hasta aquí interesado por esta raza práctica de caza, por lo que espero que encuentres al igual que muchos aficionados lo hicimos antes, la forma de trabajar colectivamente por el “jagdterrier”, en el que hemos volcado todas nuestras ilusiones.</p>
<br><br>
<p>Si no conoces al Terrier Alemán de Caza, visitando esta web, vas a comprobar varias facetas que han conseguido poner a este práctico y polivalente perro de caza en primera línea no solo en la caza bajo tierra, sino en la caza de todas las especies de caza mayor, menor y en la recuperación de animales heridos.</p>
<br><br>
<p>La principal función de la A.E.D.J.T. es la de la potenciación y selección de la raza, valiéndose no solo de la divulgación de conocimientos mediante boletines escritos, o DVDs, o libros, o …sino en una regla básica en la selección coherente, seria y responsable :</p>
<br><br>

 

<h3>LA SELECCIÓN DE LA RAZA mediante:</h3>
<br><br>

<p>1- pruebas de trabajo (pruebas de aptitudes de caza)
	<br><br>

2- test morfológicos (exámenes minuciosos y exhaustivos de posibles fallos físicos)
<br><br>

3- y tests genéticos (detectando la cruel enfermedad que causa la ceguera a ejemplares adultos, la luxación de cristalino).</p>
<br><br>
<img src="../images/esquema1.jpg" width="80%" height="auto">
<br><br>
<p>Todos estos exámenes, tienen un objetivo único, y es el de definir los ejemplares que pasando estos numerosos y rigurosos test, son declarados como APTOS para dejar su descendencia, pues reconocemos que poseen unas cualidades que destacan y son interesantes como legado a su descendencia: son los denominados <a href="#">APTOS DE CRIA</a>.</p>
<br><br>

<p>En este sentido, decir que estos exámenes no los hemos inventado nosotros, sino que siguen los mismos criterios que se marcan en la raza desde la creación de la misma, y que son contrastados y abalados por un centenar de años de trabajo continuo de muchos clubes, dando como resultado el perro práctico y polivalente , en definitiva, PURO DE TRABAJO, que conocemos hoy en día.</p>
<br><br>

<p>Esta asociación no potencia ni el reconocimiento de ejemplares que destaquen en disciplinas aisladas y sean deficientes en otras (ejemplares campeones de belleza que quizá no laten o carecen de cualquier aptitud para la caza) pues estaremos haciendo un trabajo que repercutirá negativamente en la raza a muy corto plazo, como así nos lo muestra la historia en otras razas de caza.</p>
<br><br>
 
<p>Cerrando el círculo de la selección, se encuentra: LA GESTION Y PUBLICACION DE LA INFORMACION</p>
<br><br>
<img id="esquema2" src="../images/esquema2.jpg" width="50%" height="auto">
<br><br>

<p>Para ello, la A.E.D.J.T. dispone de un potente y a la vez de muy fácil manejo programa informático, donde se recogen todas las calificaciones junto con fotografías de los ejemplares y sus pedigrees, y que se encuentra al alcance de todos y cada uno de los socios desde su propia casa, se puedan ver todas las calificaciones en trabajo, morfología o salud y de todos los ejemplares que han sido testados desde la fundación de la A.E.D.J.T.</p>
<br><br>

<p>Este programa, permite filtrar y sacar los ejemplares machos o hembras según nuestros intereses, de una u otra altura, o calificación o afijo, o número de pruebas superadas, o año de nacimiento…con sus calificaciones.</p>
<br><br>

<p>El objetivo es claro: REALIZAR LA SELECCIÓN, CON DATOS, y no con sensaciones, ni comentarios ni “famoseos”, que tienen exclusivamente oscuros objetivos mercantilistas.</p>
<br><br>

<p>Estamos convencidos de que el camino emprendido es el correcto, pues tampoco nos hemos permitido el capricho de tomar unas directrices peregrinas en cuanto a los criterios de selección, sino que compartimos el sistema ya contrastado y en funcionamiento de los clubes del DJT de todo Europa, porque la A.E.D.J.T. está dentro del Club Internacional del Deutscher Jagdterrier, y a la vez está reconocido como Club Amigo de la Real Sociedad Canina de España.</p>
<br><br>

<p>Detrás de estas líneas que me toca escribir como presidente, se encuentra un numeroso grupo de trabajo, con un interés común: el deutscher Jagdterrier como perro cazador.</p>
<br><br>

<p>Si necesitas cualquier información adicional relacionada con nuestra actividad o exclusiva de la raza o de cualquier aspecto relacionado y crees que desde la A.E.D.J.T. podemos ayudarte, no dudes en ponerte en contacto con nosotros, y ten presente, que si estás dispuesto a formar parte de la A.E.D.J.T. : tienes las puertas abiertas.</p>
<br><br>

<p>Un saludo muy cordial…</p>
